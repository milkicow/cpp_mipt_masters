#include <cassert>
#include <sstream>
#include <string>

//////////////////////////////////////////////////////////////////////////////////////////////

using namespace std::literals;

//////////////////////////////////////////////////////////////////////////////////////////////

#include "integer.hpp"

//////////////////////////////////////////////////////////////////////////////////////////////

int main() {
    Integer x = std::string(32, '1');

    Integer y = std::string(32, '2');

    //  ----------------------------------------------------------------------------------------

    assert((x += y) == "+33333333333333333333333333333333"s);
    assert((x -= y) == "+11111111111111111111111111111111"s);
    assert((x *= y) == "+246913580246913580246913580246908641975308641975308641975308642"s);
    assert((x /= y) == "+11111111111111111111111111111111"s);

    //  ----------------------------------------------------------------------------------------

    assert((x++) == "+11111111111111111111111111111111"s);
    assert((x--) == "+11111111111111111111111111111112"s);
    assert((++y) == "+22222222222222222222222222222223"s);
    assert((--y) == "+22222222222222222222222222222222"s);

    //  ----------------------------------------------------------------------------------------

    assert((x + y) == "+33333333333333333333333333333333"s);
    assert((x - y) == "-11111111111111111111111111111111"s);
    assert((x * y) == "+246913580246913580246913580246908641975308641975308641975308642"s);
    assert((x / y) == "+0"s);

    //  ----------------------------------------------------------------------------------------

    assert((x < y) == 1);
    assert((x > y) == 0);
    assert((x <= y) == 1);
    assert((x >= y) == 0);
    assert((x == y) == 0);
    assert((x != y) == 1);

    //  ----------------------------------------------------------------------------------------

    std::stringstream stream_1(std::string(32, '1'));
    std::stringstream stream_2;

    //  ----------------------------------------------------------------------------------------

    stream_1 >> x;
    stream_2 << x;

    //  ----------------------------------------------------------------------------------------

    assert(stream_2.str() == stream_1.str());

    //  ----------------------------------------------------------------------------------------

    assert(sqrt(multiply(x, x)) == x);

    //  ----------------------------------------------------------------------------------------
    //  modulo operator%=

    assert((Integer(17) %= Integer(5)) == Integer(2));
    assert((Integer(100) %= Integer(7)) == Integer(2));
    assert((Integer(0) %= Integer(3)) == Integer(0));
    assert((Integer(5) %= Integer(5)) == Integer(0));
    assert((Integer(6) %= Integer(5)) == Integer(1));

    //  ----------------------------------------------------------------------------------------
    //  modulo operator%

    assert((Integer(17) % Integer(5)) == Integer(2));
    assert((Integer(100) % Integer(7)) == Integer(2));
    assert((Integer(0) % Integer(3)) == Integer(0));
    assert((Integer(5) % Integer(5)) == Integer(0));
    assert((Integer(6) % Integer(5)) == Integer(1));

    //  ----------------------------------------------------------------------------------------
    //  modulo with large numbers

    assert((Integer("12345678901234567890") % Integer("1000000000")) == Integer("234567890"));
    assert((Integer("99999999999999999999") % Integer("99999999999999999999")) == Integer(0));
    assert((Integer("99999999999999999999") % Integer("99999999999999999998")) == Integer(1));

    //  ----------------------------------------------------------------------------------------
    //  pow

    assert(pow(Integer(2), 0u) == Integer(1));
    assert(pow(Integer(2), 1u) == Integer(2));
    assert(pow(Integer(2), 10u) == Integer(1024));
    assert(pow(Integer(3), 5u) == Integer(243));
    assert(pow(Integer(10), 5u) == Integer(100000));
    assert(pow(Integer(0), 5u) == Integer(0));
    assert(pow(Integer(1), 99u) == Integer(1));

    //  ----------------------------------------------------------------------------------------
    //  pow with large base

    assert(pow(Integer(2), 64u) == Integer("18446744073709551616"));

    //  ----------------------------------------------------------------------------------------
    //  sign

    assert(Integer(42).sign() == 1);
    assert(Integer(1).sign() == 1);
    assert(Integer(-1).sign() == -1);
    assert(Integer(-42).sign() == -1);
    assert(Integer(0).sign() == 0);

    //  ----------------------------------------------------------------------------------------
    //  abs

    assert(Integer(99).abs() == Integer(99));
    assert(Integer(-99).abs() == Integer(99));
    assert(Integer(0).abs() == Integer(0));
    assert(Integer(-1).abs() == Integer(1));

    //  ----------------------------------------------------------------------------------------
    //  abs of large negative number

    assert(Integer("-99999999999999999999").abs() == Integer("99999999999999999999"));

    //  ----------------------------------------------------------------------------------------
    //  sign does not mutate the original

    {
        Integer n("-7");
        (void)n.sign();
        assert(n == Integer(-7));
    }

    //  ----------------------------------------------------------------------------------------
    //  abs does not mutate the original

    {
        Integer n("-7");
        auto a = n.abs();
        assert(n == Integer(-7));
        assert(a == Integer(7));
    }

    //  ----------------------------------------------------------------------------------------
    //  consistency: sign and abs together

    Integer p(42), q(-42);
    assert(p.abs() == q.abs());
    assert(p.sign() == 1 && q.sign() == -1);
}

